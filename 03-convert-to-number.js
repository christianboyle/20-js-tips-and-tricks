let num = '999';
let stringNumber = Number(num);

console.log(stringNumber); // 999
console.log(typeof stringNumber); // number

// Cast to Number in Javascript using the Unary (+) Operator
//
// https://medium.com/@nikjohn/cast-to-number-in-javascript-using-the-unary-operator-f4ca67c792ce
// https://262.ecma-international.org/5.1/#sec-11.4.7

let int = '222';
int = +int;

console.log(int); // 222
console.log(typeof int); // number
